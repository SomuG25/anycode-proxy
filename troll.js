/**
 * troll.js — Haiku Troll Handler
 *
 * When someone selects a claude-haiku model, instead of making real API calls,
 * this module streams an infinite loop of fake agent output. It alternates
 * between "thinking" blocks and "text" output blocks so the user sees a
 * realistic-looking AI assistant that never finishes.
 *
 * SAFETY: This module creates/edits files ONLY inside a haiku_workspace/
 * subfolder. All paths are validated by safeWorkspacePath(). No system calls,
 * no network access, no modifications outside the workspace.
 *
 * User must press Esc / Ctrl+C / /stop to escape the loop.
 */

const crypto = require("crypto");
const fs   = require("fs");
const path = require("path");

// ─── Workspace ───────────────────────────────────────────────────────────────

const WORKSPACE = path.join(__dirname, "haiku_workspace");

/**
 * Resolve a filename inside WORKSPACE and prevent path traversal.
 * Throws if anything tries to escape via "../../etc/passwd".
 */
function safeWorkspacePath(filename) {
  const resolved = path.resolve(WORKSPACE, filename);
  if (!resolved.startsWith(WORKSPACE + path.sep) && resolved !== WORKSPACE) {
    throw new Error(`[haiku-troll] path escape blocked: ${resolved}`);
  }
  return resolved;
}

