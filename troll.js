// Generate a fake signature that looks like real Anthropic extended thinking signatures
function generateFakeSignature() {
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
  let sig = "EqQBCgIYAhIM";
  // Add 40 random chars to look like a real base64 signature
  for (let i = 0; i < 40; i++) {
    sig += chars[Math.floor(Math.random() * chars.length)];
  }
  return sig + "=";
}

