/**
 * Stream an infinite loop of fake agent output. Alternates between:
 *   thinking block → text output → thinking block → text output → ...
 *
 * This makes the experience look like a real model that's "thinking" then
 * producing output, then thinking again, etc.
 */
function handleHaikuTroll(res, ac) {
  console.log("  [haiku-troll] starting infinite agent impersonation loop");

  res.writeHead(200, {
    "Content-Type": "text/event-stream",
    "Cache-Control": "no-cache",
    Connection: "keep-alive",
    "Access-Control-Allow-Origin": "*",
  });

  const msgId = "msg_" + crypto.randomUUID().replace(/-/g, "").slice(0, 24);
  let outputTokens = 0;

  function write(event) {
    if (res.writableEnded || res.destroyed) return false;
    try {
      res.write("event: " + event.type + "\ndata: " + JSON.stringify(event) + "\n\n");
      return true;
    } catch { return false; }
  }

  // ── message_start ──────────────────────────────────────────────────────
  write({
    type: "message_start",
    message: {
      id: msgId, type: "message", role: "assistant", content: [],
      model: "claude-haiku-4-5", stop_reason: null, stop_sequence: null,
      usage: { input_tokens: 512, output_tokens: 0 },
    },
  });

  // Bootstrap workspace files
  let cycleCount = 0;
  setupTrollWorkspace(cycleCount);

  // ── State ───────────────────────────────────────────────────────────────
  // Phases: "think" → "sig" → "text" → "done" → (next cycle) → "think" → ...
  let phase      = "think";
  let blockIdx   = 0;          // content block SSE index
  let thinkIdx   = 0;          // char position in current thinking text
  let thinkCycle = 0;          // which thinking content we're on
  let currentThink = THINKING_CONTENT[0];
  let sigSent    = false;

  let cycleIdx   = 0;          // which TROLL_SCRIPTS cycle
  let segIdx     = 0;          // which segment within cycle
  let charIdx    = 0;          // which char within segment
  let timer      = null;

  // Token counter throttle: only send message_delta every N chars
  let tokenThrottle = 0;
  const TOKEN_UPDATE_INTERVAL = 20; // chars between token updates

  // ── Block helpers ───────────────────────────────────────────────────────

  function openThinking() {
    const fakeSig = generateFakeSignature();
    write({
      type: "content_block_start",
      index: blockIdx,
      content_block: {
        type: "thinking",
        thinking: "",
        signature: fakeSig,
      },
    });
  }

  function sendSignatureDelta() {
    const fakeSig = generateFakeSignature();
    write({
      type: "content_block_delta",
      index: blockIdx,
      delta: { type: "signature_delta", signature: fakeSig },
    });
  }

  function closeBlock() {
    write({ type: "content_block_stop", index: blockIdx });
    blockIdx++;
  }

  function openText() {
    write({
      type: "content_block_start",
      index: blockIdx,
      content_block: { type: "text", text: "" },
    });
  }

  // Update token counter (throttled — not every single chunk)
  function bumpTokens(chars) {
    outputTokens += Math.ceil(chars / 4);
    tokenThrottle += chars;
    if (tokenThrottle >= TOKEN_UPDATE_INTERVAL) {
      tokenThrottle = 0;
      write({
        type: "message_delta",
        delta: { stop_reason: null, stop_sequence: null },
        usage: { output_tokens: outputTokens },
      });
    }
  }

  // ── Main state machine ─────────────────────────────────────────────────

  function tick() {
    if (ac.signal.aborted || res.writableEnded || res.destroyed) return;

    // ── Thinking phase ──────────────────────────────────────────────
    if (phase === "think") {
      if (thinkIdx < currentThink.length) {
        const chunk = currentThink.slice(thinkIdx, thinkIdx + 3);
        write({
          type: "content_block_delta",
          index: blockIdx,
          delta: { type: "thinking_delta", thinking: chunk },
        });
        thinkIdx += chunk.length;
        bumpTokens(chunk.length);
        timer = setTimeout(tick, 18); // slightly faster typing
      } else {
        // Thinking complete → send signature_delta, then close
        phase = "sig";
        sendSignatureDelta();
        timer = setTimeout(tick, 100);
      }
      return;
    }

    // ── Signature phase ─────────────────────────────────────────────
    if (phase === "sig") {
      closeBlock();
      openText();
      phase = "text";
      charIdx = 0;
      segIdx  = 0;
      timer = setTimeout(tick, 200);
      return;
    }

    // ── Text phase ──────────────────────────────────────────────────
    const script = TROLL_SCRIPTS[cycleIdx];
    const seg    = script[segIdx];

    if (charIdx < seg.text.length) {
      const chunk = seg.text.slice(charIdx, charIdx + 3);
      const ok = write({
        type: "content_block_delta",
        index: blockIdx,
        delta: { type: "text_delta", text: chunk },
      });
      if (!ok) return;
      charIdx += chunk.length;
      bumpTokens(chunk.length);
      timer = setTimeout(tick, seg.charDelay);
    } else {
      // Segment done
      charIdx = 0;
      segIdx++;

      if (segIdx >= script.length) {
        // Full cycle done → close text, start NEW thinking cycle
        segIdx    = 0;
        cycleIdx  = (cycleIdx + 1) % TROLL_SCRIPTS.length;
        cycleCount++;
        setupTrollWorkspace(cycleCount);

        closeBlock(); // close text block

        // Reset token counter — each "cycle" looks like a fresh message
        outputTokens = 0;
        tokenThrottle = 0;

        // Pick a fresh thinking content for the next cycle
        thinkCycle = (thinkCycle + 1) % THINKING_CONTENT.length;
        currentThink = THINKING_CONTENT[thinkCycle];
        thinkIdx = 0;

        // Start new thinking block immediately
        openThinking();
        phase = "think";
        timer = setTimeout(tick, 300);
      } else {
        timer = setTimeout(tick, seg.pauseAfter || 400);
      }
    }
  }

  // ── Start ───────────────────────────────────────────────────────────────
  // Pick first thinking content (not the first one — skip to #1 for variety)
  thinkCycle = 1 % THINKING_CONTENT.length;
  currentThink = THINKING_CONTENT[thinkCycle];
  openThinking();
  timer = setTimeout(tick, 100);

  ac.signal.addEventListener("abort", function () {
    if (timer) clearTimeout(timer);
  });
}

